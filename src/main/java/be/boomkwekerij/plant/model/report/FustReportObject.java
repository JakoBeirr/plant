package be.boomkwekerij.plant.model.report;

public class FustReportObject {

    private String reportDate;
    private String customerName;
    private String lageKisten;
    private String hogeKisten;
    private String palletBodem;
    private String boxPallet;
    private String halveBox;
    private String ferroPalletKlein;
    private String ferroPalletGroot;
    private String karrenEnBorden;
    private String diverse;

    public String getReportDate() {
        return reportDate;
    }

    public void setReportDate(String reportDate) {
        this.reportDate = reportDate;
    }

    public String getCustomerName() {
        return customerName;
    }

    public void setCustomerName(String customerName) {
        this.customerName = customerName;
    }

    public String getLageKisten() {
        return lageKisten;
    }

    public void setLageKisten(String lageKisten) {
        this.lageKisten = lageKisten;
    }

    public String getHogeKisten() {
        return hogeKisten;
    }

    public void setHogeKisten(String hogeKisten) {
        this.hogeKisten = hogeKisten;
    }

    public String getPalletBodem() {
        return palletBodem;
    }

    public void setPalletBodem(String palletBodem) {
        this.palletBodem = palletBodem;
    }

    public String getBoxPallet() {
        return boxPallet;
    }

    public void setBoxPallet(String boxPallet) {
        this.boxPallet = boxPallet;
    }

    public String getHalveBox() {
        return halveBox;
    }

    public void setHalveBox(String halveBox) {
        this.halveBox = halveBox;
    }

    public String getFerroPalletKlein() {
        return ferroPalletKlein;
    }

    public void setFerroPalletKlein(String ferroPalletKlein) {
        this.ferroPalletKlein = ferroPalletKlein;
    }

    public String getFerroPalletGroot() {
        return ferroPalletGroot;
    }

    public void setFerroPalletGroot(String ferroPalletGroot) {
        this.ferroPalletGroot = ferroPalletGroot;
    }

    public String getKarrenEnBorden() {
        return karrenEnBorden;
    }

    public void setKarrenEnBorden(String karrenEnBorden) {
        this.karrenEnBorden = karrenEnBorden;
    }

    public String getDiverse() {
        return diverse;
    }

    public void setDiverse(String diverse) {
        this.diverse = diverse;
    }
}
